# Operadores de comparação

# == Igualdade
# != Diferente
# > Maior
# < Menor
# >= Maior ou igual
# <= Menor ou igual

from ast import And


n1 = float(input('Digite o primeiro número: '))
n2 = float(input('Digite o segundo número: '))
print()

print(n1, '==', n2, '->', n1 == n2)
print()

print(n1, '!=', n2, '->', n1 != n2)
print()

print(n1, '>', n2, '->', n1 > n2)
print()

print(n1, '<', n2, '->', n1 < n2)
print()

print(n1, '>=', n2, '->', n1 >= n2)
print()

print(n1, '<=', n2, '->', n1 <= n2)
